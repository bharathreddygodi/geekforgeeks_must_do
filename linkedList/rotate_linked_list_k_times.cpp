class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        int count = 0;
        int i = 0;
        Node* h = NULL;
        Node* tail = NULL;
        Node* curr = head;

        // Get size
        while (curr != NULL)
        {
            count++;
            tail = curr;
            curr = curr->next;
        }

        if ((k % count) == 0)
        {
            return head;
        }

        curr = head;
        while (i < k-1)
        {
            i++;
            curr = curr->next;
        }

        h = curr->next;
        curr->next = NULL;
        tail->next = head;
        return h;
    }
};
    


